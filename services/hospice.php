				<h3>Purpose</h3>
				<div class="row">
				<div class="col-md-7">
				<p>
					O’Neill Healthcare Hospice started in 2011 by the O’Neill family as a way to provide extra care and comfort to the residents of their facilities and the surrounding area, when they are near the end stages of life. The hospice staff, working closely with the facility staff, becomes part of the team to deliver care and enhance the quality of life of the resident.
				</p>
				
				<h3>Mission</h3>
				<p>
					O’Neill Healthcare Hospice’s mission is to help residents achieve the highest level of comfort and quality of life possible while remaining in the security of their own environment.
				</p>

				<h3>What is Hospice Care?</h3>
				<p>
					Hospice care is a specialized service focusing on alleviating physical, emotional, and spiritual symptoms of discomfort from a life-limiting illness. Comfort care addresses the symptoms of an illness rather than employ aggressive treatments for curative measures. A team of specialists provides guidance and support to both the patient and the family. The hospice staff works with the resident and family to discuss goals, make a plan of care, and facilitate meeting those goals and provide comfort.
				</p>
				
				</div>
				<div class="col-md-5">
					<img src="../assets/images/hospice2.jpg" alt="Our resident reading a book" />
				</div>	
				</div>
				

				<h3>Features of O’Neill Healthcare Hospice Care</h3>

				<ul>
					<li>Hospice services are provided in the facility, as this is the resident’s home.</li>
					<li>Our specially trained hospice team includes:
						<ul>
							<li>nurse case manager</li>
							<li>hospice physicians</li>
							<li>nurse practitioners</li>
							<li>licensed nurses</li>
							<li>state-tested nursing assistants</li>
							<li>social workers</li>
							<li>chaplain</li>
							<li>volunteers</li>
						</ul>
					</li>
					<li>An on-call staff member is available 24 hours a day, to support the resident, family and facility staff as needed.</li>
					<li>The family and resident can receive Anticipatory grief support and Bereavement follow-up, to help cope with the loss and emotions they are experiencing.</li>
					<li>CHAP certified</li>
					<li>Member of the Midwest Care Alliance</li>
				</ul>
				
				<h3>When to choose Hospice Care</h3>

				<p>
					Serious illness can take a toll on the body. After many different interventions and medications, there may be little to no change noted. When a resident has reached the point where aggressive, curative treatments are no longer effective, it may be time for comfort care with hospice.
				</p>
				
				<p>Signs that hospice care may be needed include:</p>

				<ul>
					<li>Decreased appetite with significant weight loss</li>
					<li>Sleeping excessively</li>
					<li>Loss of mobility</li>
					<li>Inability to independently eat or perform personal care</li>
					<li>Increased discomfort/pain</li>
					<li>Little or no interaction/verbal communication</li>
				</ul>
				
				<div class="row">
				<div class="col-md-7">
				<h3>Who can receive Hospice Care?</h3>

				<p>Many illnesses progress to the point where hospice care is beneficial. General criteria to qualify for hospice care are:</p>

				<ul>
					<li>A physician has issued a diagnosis of a life-limiting illness</li>
					<li>Resident is no longer a candidate for, or no longer wishes to receive, curative treatment</li>
				</ul>
				</div>
				<div class="col-md-5">
				<img src="../assets/images/hospice.jpg" alt="Caring hands" />
				</div>
				</div>
				<h3>What is the cost of Hospice Care?</h3>

				<ul>
					<li>Medicare Part A covers hospice services 100%. The Medicare Hospice benefits include personal care from the hospice staff, medical equipment, wound care, medications, counseling, and supplies for the terminal illness and related conditions.</li>
					<li>Many private insurances include hospice coverage, which varies for each policy.</li>
					<li>Out-of-pocket costs, if any, are minimal.</li>
				</ul>

