				<h3>Overview of Benefits</h3>
				<p>
					Employees working at least 30 hours per week are considered Full Time employees.  Full Time employees are eligible for benefits on the first day of the month following 60 days of employment.
				</p>
				<ul>
					<li>
						Vacation Pay/Paid Time Off (PTO) - An employee is eligible for vacation/PTO benefits after completing one year of employment and every year thereafter on his/her anniversary date.  Vacation/PTO is earned based on the number of hours worked.  Full Time employees that work at least 2000 hours per year will earn a maximum of:
						<ul>
							<li>96 hours after 1 year of service</li>
							<li>136 hours after 5 years of service</li>
							<li>176 hours after 10 years of service</li>
						</ul>
					</li>
					<li>7 Paid Holidays - Holiday pay is earned for Memorial Day, Independence Day, Labor Day, Thanksgiving Day, Christmas Day, New Year's Day, and the Employee's Birthday.</li>
					<li>Health Insurance - Optional group health insurance is available for Full Time employees.  Premium discounts are available for employees who participate in the Wellness Program.</li>
					<li>Flexible Spending Account - This option allows eligible employees to use a portion of their gross income (before taxes) toward health and dental expenses.</li>
					<li>
						401(k) Retirement Savings
							<ul>
								<li>Employee contributions begin immediately, tax-deferred.</li>
								<li>After one year of employment, the facility will match 25% of an employee’s deferrals, up to 6% of salary contributions.</li>
								<li>Employee will receive an annual profit sharing contribution after one year.</li>
							</ul>
					</li>
					<li>Attendance Bonus - All employees, excluding managers and office clerical staff, may be eligible to be awarded an attendance bonus, paid every two weeks.</li>
					<li>Wellness Bonus - A semi-annual Wellness Bonus will be paid to qualifying employees in July and December.</li>
					<li>Uniform Allowance - Nursing, Dietary, Housekeeping, Laundry, and Maintenance employees are paid an hourly uniform allowance.</li>
					<li>Weekend Shift Differential - All hourly employees working the weekend will receive a shift differential for regular and overtime hours worked.</li>
					<li>Club 5 and Club 10 – Additional paid time off is available to employees after five years of service and ten years of service.</li>
					<li>Dental Insurance - Optional group dental insurance is available for Full Time employees.</li>
					<li>Life Insurance - Each Full Time employee is eligible for a $10,000 life insurance policy at no charge to him/her.  Additional voluntary life insurance policies are also available.</li>
					<li>Other Insurance - There are other insurance policies available to employees, such as short-term and long-term disability, cancer insurance, accident insurance, etc.</li>
					
				</ul>

